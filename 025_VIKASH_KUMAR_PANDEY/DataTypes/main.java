package DataTypes;

public class main {
    public static void main(String args[]){

        int a = 25;     //Integer variables
        int b = 25;     //Integer variables
        System.out.println("Integer : " + (a+b));

        String name = "Vikash";     //String variable
        System.out.println("String : " +name);

        float val1 = 3.14f;      //Single precission floating point variable
        System.out.println("Float : " + val1);

        double val2 = 3.14;      //Double precission floating point variable
        System.out.println("Double : " +val2);

        char c = '$';    //Character variable
        System.out.println("Character : " +c);

        boolean yes = true;     //Boolean variable
        System.out.println("Boolean : " +yes);
        boolean no = false;     //Boolean variable
        System.out.println("Boolean : " +no);

    }
}
